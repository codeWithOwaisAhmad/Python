# Tutorial: Basics of Python - Datatypes

# Datatypes in Python
# Python supports several built-in datatypes, including:
# - Numbers (integers, floats, complex)
# - Strings
# - Lists
# - Tuples
# - Sets
# - Dictionaries
# - Booleans

# 1. Numbers
# Numbers in Python can be integers, floats, or complex numbers.

# Integer
x = 10
print("Integer x:", x)

# Float
y = 3.14
print("Float y:", y)

# Complex Number
z = complex(2, 3)  # 2 + 3j
print("Complex Number z:", z)

# 2. Strings
# Strings are sequences of characters.

# String Assignment
greeting = "Hello, World!"
print("Greeting:", greeting)

# String with Special Characters
phrase = "Python programming"
print("Phrase:", phrase)

# 3. Lists
# Lists are ordered collections of items.

# Creating a List
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# 4. Tuples
# Tuples are ordered collections of items that are immutable.

# Creating a Tuple
coordinates = (10, 20)
print("Coordinates:", coordinates)

# 5. Sets
# Sets are unordered collections of unique items.

# Creating a Set
unique_numbers = {1, 2, 3, 4, 5}
print("Unique Numbers:", unique_numbers)

# 6. Dictionaries
# Dictionaries are collections of key-value pairs.

# Creating a Dictionary
student = {"name": "Alice", "age": 25, "city": "New York"}
print("Student:", student)

# 7. Booleans
# Booleans represent True or False values.

# Boolean Values
is_active = True
print("Is Active:", is_active)

is_logged_in = False
print("Is Logged In:", is_logged_in)
